export function getTelegramDailyReportsForSite(
  siteId: string,
): readonly DailyTelegramReport[] {
  return MOCK_DAILY_TELEGRAM_REPORTS.filter((r) => r.siteId === siteId).sort((a, b) => {
    const ta = a.reportedAtIso ? new Date(a.reportedAtIso).getTime() : 0
    const tb = b.reportedAtIso ? new Date(b.reportedAtIso).getTime() : 0
    if (tb !== ta) return tb - ta
    return b.sequence - a.sequence
  })
}
